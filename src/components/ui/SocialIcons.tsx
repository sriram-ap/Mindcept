export function SocialGlyph({
  name,
  className = "h-5 w-5",
}: {
  name: string;
  className?: string;
}) {
  const glyph = name === "whatsapp" ? WHATSAPP_GLYPH : GLYPHS[name];
  if (!glyph) return null;
  return (
    <svg
      viewBox="0 0 24 24"
      fill="currentColor"
      className={className}
      aria-hidden="true"
      dangerouslySetInnerHTML={{ __html: glyph }}
    />
  );
}

